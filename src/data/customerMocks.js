export function getCustomerListSummary(rows, scopeLabel = '고객') {
  const contractCustomers = rows.filter((row) => row.customerStatus === CUSTOMER_STATUS.CONTRACT).length
  const prospectCustomers = rows.filter((row) => row.customerStatus === CUSTOMER_STATUS.PROSPECT).length
  const interestCustomers = rows.filter((row) => row.customerStatus === CUSTOMER_STATUS.INTEREST).length

  return [
    {
      icon: 'mdi-account-group-outline',
      value: rows.length.toLocaleString('ko-KR'),
      unit: '명',
      label: `${scopeLabel} 수`,
      accent: '#2563eb',
      tone: '#eff6ff',
    },
    {
      icon: 'mdi-file-document-outline',
      value: contractCustomers.toLocaleString('ko-KR'),
      unit: '명',
      label: '계약 고객',
      accent: '#f97316',
      tone: '#fff7ed',
    },
    {
      icon: 'mdi-sprout-outline',
      value: prospectCustomers.toLocaleString('ko-KR'),
      unit: '명',
      label: '잠재 고객',
      accent: '#16a34a',
      tone: '#ecfdf3',
    },
    {
      icon: 'mdi-alert-outline',
      value: interestCustomers.toLocaleString('ko-KR'),
      unit: '명',
      label: '관심 고객',
      accent: '#f59e0b',
      tone: '#fff8e6',
    },
  ]
}
